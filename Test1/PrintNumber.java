
public class PrintNumber {
	int n;
	float f;
	double d;
	long l;
	
	public void print(int n)
	{
		System.out.println("You have entered Integer number "+n);
	}
	public void print(float f)
	{
		System.out.println("You have entered Float number "+f);
	}

	public void print(double d)
	{
		System.out.println("You have entered Double number "+d);
	}

	public void print(long l)
	{
		System.out.println("You have entered Long number "+l);
	}

}
